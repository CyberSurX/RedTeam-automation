import dotenv from 'dotenv';
import axios from 'axios';

dotenv.config({ path: '.env.thundernight' });

const apiKey = process.env.HACKERONE_API_KEY;
const email = process.env.HACKERONE_EMAIL;
const username = process.env.HACKERONE_USERNAME;

console.log('🔍 HackerOne Gerçek Rapor Gönderme Testi Başlıyor...');
console.log('👤 Kullanıcı:', username);
console.log('📧 Email:', email);

const baseURL = 'https://api.hackerone.com/v1';

async function testRealReport() {
  try {
    console.log('🧪 API anahtarı test ediliyor...');
    console.log('📧 Kullanılan email:', email);
    console.log('🔐 API Key uzunluğu:', apiKey ? apiKey.length : 'Tanımsız');
    
    // HackerOne auth: Basic auth with username:API_KEY
    const basicAuth = Buffer.from(username + ':' + apiKey).toString('base64');
    console.log('🔑 Base64 Auth:', basicAuth.substring(0, 20) + '...');
    
    // Test için önce programs endpoint'ini deneyelim (/me çalışmayabilir)
    console.log('🔍 HackerOne API testi (/hackers/programs)...');
    const programsResponse = await axios.get(baseURL + '/hackers/programs', {
      headers: {
        'Authorization': 'Basic ' + basicAuth,
        'Accept': 'application/json',
        'User-Agent': 'RedTeamAutomation/1.0'
      }
    });

    const programs = programsResponse.data.data;
    console.log('✅ API bağlantısı başarılı!');
    console.log('✅ Bulunan program sayısı:', programs.length);
    
    // Programları göster
    console.log('\n📊 İlk 5 Program:');
    programs.slice(0, 5).forEach((p, i) => {
      console.log(`#${i + 1}: ${p.attributes.name} (${p.attributes.handle})`);
    });

    // Raporları kontrol edelim
    console.log('📋 Mevcut raporlar kontrol ediliyor...');
    const reportsResponse = await axios.get(baseURL + '/hackers/reports', {
      headers: {
        'Authorization': 'Basic ' + basicAuth,
        'Accept': 'application/json',
        'User-Agent': 'RedTeamAutomation/1.0'
      }
    });

    console.log('✅ Mevcut rapor sayısı:', reportsResponse.data.data.length);
    
    // Şimdi GERÇEK bir test raporu göndereceğiz
    console.log('\n🚀 GERÇEK RAPOR GÖNDERME DENEMESİ BAŞLIYOR...');
    
    // Test programı seç
    const testProgram = programs.find(p => 
      p.attributes.handle === 'security' || 
      p.attributes.name.toLowerCase().includes('hackerone')
    ) || programs[0];

    console.log(`🎯 Seçilen program: ${testProgram.attributes.name}`);
    
    // Gerçek rapor gönderme
    const reportData = {
      data: {
        type: 'report',
        attributes: {
          title: 'Test: Automated Security System Validation',
          summary: 'This is a test report generated by automated security testing system. Please disregard.',
          severity_rating: 'low',
          team_handle: testProgram.attributes.handle,
          vulnerability_information: 'Test submission for system validation purposes'
        }
      }
    };

    console.log('📝 Rapor gönderiliyor (GERÇEK)...');
    const reportResponse = await axios.post(baseURL + '/hackers/reports', reportData, {
      headers: {
        'Authorization': 'Basic ' + basicAuth,
        'Accept': 'application/json',
        'Content-Type': 'application/json',
        'User-Agent': 'RedTeamAutomation/1.0'
      }
    });

    console.log('✅ RAPOR BAŞARIYLA GÖNDERİLDİ!');
    console.log('📋 Rapor ID:', reportResponse.data.data.id);
    console.log('🔗 Rapor URL: https://hackerone.com/reports/' + reportResponse.data.data.id);
    console.log('📊 Rapor Durumu:', reportResponse.data.data.attributes.state);
    console.log('⏰ Gönderim Zamanı:', new Date().toISOString());
    
    console.log('\n🎉 GERÇEK DÜNYA TESTİ TAMAMLANDI!');
    console.log('✅ Gerçek rapor HackerOne sistemine gönderildi');
    console.log('✅ Otomatik raporlama sistemi çalışıyor');
    console.log('✅ Sistem gerçek dünyada aktif');

  } catch (error) {
    console.error('\n❌ HATA:', error.response?.status, error.response?.data?.errors?.[0]?.title || error.message);
    
    if (error.response?.status === 401) {
      console.log('🔐 Kimlik doğrulama hatası - Sebepler:');
      console.log('   - API anahtarı geçersiz veya süresi dolmuş');
      console.log('   - Email adresi yanlış');
      console.log('   - HackerOne hesabında API erişimi kapalı');
      console.log('   - API anahtarının doğru formatta olmaması');
    } else if (error.response?.status === 403) {
      console.log('🚫 Erişim engellendi - HackerOne hesap ayarlarını kontrol edin');
    } else {
      console.log('🔧 Teknik detaylar:');
      console.log('   Status:', error.response?.status);
      console.log('   Hata:', error.response?.data);
    }
  }
}

testRealReport();
